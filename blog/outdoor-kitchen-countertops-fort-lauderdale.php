                <h2>Frequently Asked Questions</h2>

                <div class="accordion" id="faqAccordion">
                    <div class="accordion-item">
                        <button class="accordion-header" aria-expanded="true">
                            <span>Can quartz countertops be used in outdoor kitchens?</span>
                            <i class="bi bi-chevron-down accordion-icon"></i>
                        </button>
                        <div class="accordion-content">
                            <p>Quartz countertops can be used in covered outdoor kitchens where they are protected from direct sunlight. Prolonged UV exposure can cause discoloration in quartz, so it is best suited for lanais, covered patios, and shaded pool areas. For fully exposed outdoor kitchens, granite or concrete are better alternatives.</p>
                        </div>
                    </div>
                    <div class="accordion-item">
                        <button class="accordion-header" aria-expanded="false">
                            <span>What is the best countertop material for Fort Lauderdale outdoor kitchens?</span>
                            <i class="bi bi-chevron-down accordion-icon"></i>
                        </button>
                        <div class="accordion-content">
                            <p>The best material depends on your setup. For covered outdoor kitchens, quartz offers excellent durability and low maintenance. For fully exposed areas, natural granite is ideal because it resists UV rays, heat, and weather naturally. Concrete countertops are also popular for their customizable appearance and weather resistance when properly sealed.</p>
                        </div>
                    </div>
                    <div class="accordion-item">
                        <button class="accordion-header" aria-expanded="false">
                            <span>How do I protect outdoor countertops from Florida weather?</span>
                            <i class="bi bi-chevron-down accordion-icon"></i>
                        </button>
                        <div class="accordion-content">
                            <p>Protect outdoor countertops by choosing appropriate materials for your exposure level, using covers during hurricanes and severe weather, applying sealant annually for natural stone and concrete, and cleaning regularly to prevent salt air buildup. For waterfront properties near the Intracoastal, rinse surfaces frequently to remove salt residue.</p>
                        </div>
                    </div>
                    <div class="accordion-item">
                        <button class="accordion-header" aria-expanded="false">
                            <span>Do outdoor countertops need to be sealed?</span>
                            <i class="bi bi-chevron-down accordion-icon"></i>
                        </button>
                        <div class="accordion-content">
                            <p>It depends on the material. Granite outdoor countertops should be sealed annually to maintain stain resistance and protect against moisture penetration. Concrete countertops require sealing every 1-3 years. Quartz countertops do not require sealing as they are non-porous, but remember quartz should only be used in covered outdoor spaces.</p>
                        </div>
