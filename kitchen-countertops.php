                totalLinearFt += (calcState.islandLength * 2 + calcState.islandDepth * 2) / 12;
            }

            if (calcState.kitchenType === 'perimeter' || calcState.kitchenType === 'both' || calcState.kitchenType === 'remodel') {
                perimeterSqft = (calcState.perimeterLength * 12 * calcState.perimeterDepth) / 144;
                totalLinearFt += calcState.perimeterLength;
            }

            if (calcState.backsplashHeight > 0) {
                let backsplashLength = 0;
                if (calcState.kitchenType === 'perimeter' || calcState.kitchenType === 'both' || calcState.kitchenType === 'remodel') {
                    backsplashLength = calcState.perimeterLength * 12;
                }
                backsplashSqft = (backsplashLength * calcState.backsplashHeight) / 144;
            }

            return {
                islandSqft,
                perimeterSqft,
                backsplashSqft,
                totalSqft: islandSqft + perimeterSqft + backsplashSqft,
                totalLinearFt
            };
        }

        function updateSummary() {
            const dims = calculateSquareFootage();

            // Calculate costs
            const materialCost = dims.totalSqft * calcState.quartzPrice;
            const edgeCost = dims.totalLinearFt * calcState.edgePrice;

            let featuresCost = 0;
            let featuresText = [];

            if (calcState.features.waterfall.selected) {
                const waterfallCost = calcState.features.waterfall.price * calcState.features.waterfall.count;
                featuresCost += waterfallCost;
                featuresText.push(`Waterfall (${calcState.features.waterfall.count} side${calcState.features.waterfall.count > 1 ? 's' : ''})`);
            }
            if (calcState.features.cooktop.selected) {
                featuresCost += calcState.features.cooktop.price;
                featuresText.push('Cooktop Cutout');
            }
            if (calcState.features.sink.selected) {
                featuresCost += calcState.features.sink.price;
                featuresText.push('Sink Cutout');
            }
            if (calcState.features.seam.selected) {
                featuresCost += calcState.features.seam.price;
                featuresText.push('Seam Minimization');
            }

            const typeCost = calcState.kitchenTypePrice;
            const installBase = 300;

            const lowEstimate = Math.round(materialCost + edgeCost + featuresCost + typeCost + installBase);
            const highEstimate = Math.round(lowEstimate * 1.25);

            const typeLabels = {
                'island': 'Island Only',
                'perimeter': 'Perimeter Only',
                'both': 'Island + Perimeter',
                'remodel': 'Full Remodel'
            };

            const edgeLabels = {
                'eased': 'Eased Edge',
                'beveled': 'Beveled Edge',
                'bullnose': 'Bullnose Edge',
                'ogee': 'Ogee Edge'
            };

            document.getElementById('summaryType').textContent = typeLabels[calcState.kitchenType];

            if (calcState.kitchenType === 'island' || calcState.kitchenType === 'both' || calcState.kitchenType === 'remodel') {
                document.getElementById('summaryIsland').textContent = `${calcState.islandLength}" x ${calcState.islandDepth}" (${dims.islandSqft.toFixed(1)} sq ft)`;
            } else {
                document.getElementById('summaryIsland').textContent = 'N/A';
            }

            if (calcState.kitchenType === 'perimeter' || calcState.kitchenType === 'both' || calcState.kitchenType === 'remodel') {
                document.getElementById('summaryPerimeter').textContent = `${calcState.perimeterLength} linear ft (${dims.perimeterSqft.toFixed(1)} sq ft)`;
            } else {
                document.getElementById('summaryPerimeter').textContent = 'N/A';
            }

            document.getElementById('summarySqft').textContent = `${dims.totalSqft.toFixed(2)} sq ft`;
            document.getElementById('summaryColor').textContent = calcState.quartzColor;
            document.getElementById('summaryEdge').textContent = edgeLabels[calcState.edgeProfile];
            document.getElementById('summaryFeatures').textContent = featuresText.length > 0 ? featuresText.join(', ') : 'None';
            document.getElementById('summaryBacksplash').textContent = calcState.backsplashHeight > 0 ? `${calcState.backsplashHeight}" Height` : 'None';
            document.getElementById('summaryTotal').textContent = `$${lowEstimate.toLocaleString()} - $${highEstimate.toLocaleString()}`;

            // Store for PDF
            calcState.lowEstimate = lowEstimate;
            calcState.highEstimate = highEstimate;
            calcState.totalSqft = dims.totalSqft;
            calcState.featuresText = featuresText;
            calcState.dims = dims;
        }

