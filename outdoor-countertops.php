                document.querySelectorAll('.material-card').forEach(c => c.classList.remove('selected'));
                card.classList.add('selected');
                calcState.material = card.dataset.material;
                calcState.materialPrice = parseInt(card.dataset.price) || 75;
            });
        });

        // Feature Checkboxes
        document.querySelectorAll('.feature-checkbox').forEach(checkbox => {
            checkbox.addEventListener('click', () => {
                checkbox.classList.toggle('selected');
                const feature = checkbox.dataset.feature;
                if (checkbox.classList.contains('selected')) {
                    if (!calcState.features.includes(feature)) {
                        calcState.features.push(feature);
                    }
                } else {
                    calcState.features = calcState.features.filter(f => f !== feature);
                }
            });
        });

        // Edge Selection
        document.querySelectorAll('[data-edge-price]').forEach(card => {
            card.addEventListener('click', () => {
                document.querySelectorAll('[data-edge-price]').forEach(c => c.classList.remove('selected'));
                card.classList.add('selected');
                calcState.edgeProfile = card.dataset.value;
                calcState.edgePrice = parseInt(card.dataset.edgePrice) || 0;
            });
        });

        function updateSummary() {
            const sqft = (calcState.length * calcState.depth) / 144;

            // Calculate material cost based on selected material
            let materialPricePerSqft = calcState.materialPrice;
            const materialCost = sqft * materialPricePerSqft;

            // Calculate edge cost
            const edgeCost = calcState.edgeLinearFt * calcState.edgePrice;

            // Calculate feature costs
            let featureCost = 0;
            calcState.features.forEach(feature => {
                featureCost += featurePrices[feature] || 0;
            });

            // Space type premium
            const typeCost = calcState.spaceTypePrice;

            // Base installation
            const installBase = 200;

            const lowEstimate = Math.round(materialCost + edgeCost + featureCost + typeCost + installBase);
            const highEstimate = Math.round(lowEstimate * 1.25);

            const typeLabels = {
                'island': 'Outdoor Kitchen Island',
                'poolbar': 'Pool Bar',
                'bbq': 'BBQ Station',
                'full': 'Full Outdoor Kitchen',
                'lshaped': 'L-Shaped Kitchen'
            };

            const materialLabels = {
                'standard': 'Standard Quartz (Covered)',
                'premium': 'Premium Outdoor-Rated',
                'dekton': 'Dekton/Ultracompact'
            };

            const exposureLabels = {
                'covered': 'Covered/Shaded',
                'partial': 'Partial Sun',
                'fullsun': 'Full Sun Exposure'
            };

            const edgeLabels = {
                'eased': 'Eased Edge',
                'beveled': 'Beveled Edge',
                'bullnose': 'Bullnose Edge',
                'ogee': 'Ogee Edge'
            };

            const featureLabels = {
                'grill': 'Grill Cutout',
                'sink': 'Sink Cutout',
                'burner': 'Burner Cutout',
                'waterfall': 'Waterfall Edge'
            };

            document.getElementById('summaryType').textContent = typeLabels[calcState.spaceType];
            document.getElementById('summaryDimensions').textContent = `${calcState.length}" x ${calcState.depth}"`;
            document.getElementById('summarySqft').textContent = `${sqft.toFixed(2)} sq ft`;
            document.getElementById('summaryMaterial').textContent = materialLabels[calcState.material];
            document.getElementById('summaryExposure').textContent = exposureLabels[calcState.coverageType];
            document.getElementById('summaryEdge').textContent = edgeLabels[calcState.edgeProfile];

            const featuresText = calcState.features.length > 0
                ? calcState.features.map(f => featureLabels[f]).join(', ')
                : 'None selected';
            document.getElementById('summaryFeatures').textContent = featuresText;

            document.getElementById('summaryTotal').textContent = `$${lowEstimate.toLocaleString()} - $${highEstimate.toLocaleString()}`;

            // Store for PDF
            calcState.lowEstimate = lowEstimate;
            calcState.highEstimate = highEstimate;
            calcState.totalSqft = sqft;
        }

