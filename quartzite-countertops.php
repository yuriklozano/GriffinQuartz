                    <ul>
                        <li>Seal your quartzite upon installation and reseal annually</li>
                        <li>Clean daily with mild soap, warm water, and a soft cloth</li>
                        <li>Blot spills promptly, especially acidic liquids like wine or citrus</li>
                        <li>Use cutting boards to protect the surface and your knives</li>
                        <li>Use trivets under hot items to protect the sealant</li>
                        <li>Test sealer effectiveness with water droplet test annually</li>
                    </ul>
                </div>
                <div class="care-card">
                    <h3><i class="bi bi-x-circle"></i> Don'ts</h3>
                    <ul>
                        <li>Don't use acidic cleaners (vinegar, lemon, ammonia)</li>
                        <li>Don't let spills sit for extended periods</li>
                        <li>Don't use abrasive scrubbers or harsh chemicals</li>
                        <li>Don't skip the annual sealing maintenance</li>
                        <li>Don't confuse quartzite with quartz (different care needs)</li>
                        <li>Don't use generic stone cleaners without checking pH level</li>
                    </ul>
